package com.company;

public class RelationalOperator {
    public static void main(String[] args){
        int a=10;
        int b=100;
        int c=10;

        System.out.println("a>b : "+(a>b));
        System.out.println("a<b : "+(a<b));
        System.out.println("a>=c : "+(a>=c));
        System.out.println("a<=b : "+(a<=b));
        System.out.println("a==c : "+(a==c));
        System.out.println("a!=c : "+(a!=c));
    }
}
