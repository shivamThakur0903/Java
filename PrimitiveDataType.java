package com.company;

// Primitive datatype int, short, long, float, double, char, byte, boolean,
public class PrimitiveDataType {
    public static void main(String[] args) {
        int age = 21;
        short weight = 80;
        long number = 963113;
        float height = 5.9f;
        double salary = 35595.50d;
        char Name = 'S';
        byte House_No = 102;

        System.out.println("Name Starts With: " + Name);
        System.out.println("Age is : "+age);
        System.out.println("Height is : "+height);
        System.out.println("Weight is : "+weight+"KG");
        System.out.println("Number is : "+number);
        System.out.println("Salary is : "+salary);
        System.out.println("House No. : "+House_No);
    }
}
